export function getMockBooths(sort: BoothSort) {
  const booths = [...mockBooths];

  if (sort === 'waiting-asc') {
    return booths.sort((a, b) => a.currentWaitingTeams - b.currentWaitingTeams);
  }

  return booths.sort((a, b) => b.likeCount - a.likeCount);
}

export function getMockMapBooths(): BoothMapItem[] {
  return mockBooths.map(({ boothId, name, xRatio, yRatio, type, color }) => ({
    boothId,
    name,
    xRatio,
    yRatio,
    type,
    color,
  }));
}

export function getMockBooth(boothId: number) {
  return mockBooths.find((booth) => booth.boothId === boothId);
}
